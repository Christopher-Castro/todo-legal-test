<script>
import axios from 'axios'
export default {
  name: 'Home',
  components: {

  },
  data: function () {
    return {
      mensaje: 'Mensaje desde Vue.js',
      usuario: '',
      password: '',
      error: false,
      error_msg: '',
      show: true
    }
  },
  computed: {
    validation () {
      return this.password.length > 4 && this.password.length < 13
    }
  },
  methods: {
    getUser () {
      const path = 'http://localhost:5000/home'
      axios.get(path).then((respuesta) => {
        this.mensaje = respuesta.data
        console.log(respuesta.data)
      }).catch((error) => {
        console.error(error)
      })
    },
    login () {
      let json = {
        'username': this.usuario,
        'password': this.password
      }
      axios.post('http://localhost:5000/auth/login', json)
        .then(data => {
          if (data.data.state === true) {
            localStorage.client_id = data.data.id
            this.$router.push('dashboard')
          } else {
            this.error = true
            this.error_msg = data.data.error_msg
            console.log(this.error_msg)
          }
        })
    }
  },
  created () {
    this.getUser()
  }
}
</script>

